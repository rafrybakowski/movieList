import UIKit

extension UIFont {
    
    static var head28: UIFont {
        return .boldSystemFont(ofSize: 28)
    }
    
    static var head24: UIFont {
        return .boldSystemFont(ofSize: 24)
    }
    
    static var head20: UIFont {
        return .boldSystemFont(ofSize: 20)
    }
    
    static var head16: UIFont {
        return .boldSystemFont(ofSize: 16)
    }
    
    static var plain20: UIFont {
        return .systemFont(ofSize: 20)
    }
    
    static var plain16: UIFont {
        return .systemFont(ofSize: 16)
    }
    
    static var plain12: UIFont {
        return .systemFont(ofSize: 14)
    }
}
